package boletin07_2;

public class condicional {
    public void verOperacion(short num1,short num2){
        
        if(num1>=num2){
            System.out.println(num1+" menos "+num2+" = "+(num1-num2));
        }
        else{
            System.out.println(num1+" mas "+num2+" = "+(num1+num2));
        }
        
    }
}
